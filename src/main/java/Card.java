public class Card {
    private int Type;
    private int Pin;
    private double Balance;

    public int getType() {
        return Type;
    }

    public void setType(int type) {
        Type = type;
    }

    public int getPin() {
        return Pin;
    }

    public void setPin(int pin) {
        Pin = pin;
    }

    public double getBalance() {
        return Balance;
    }

    public void setBalance(double balance) {
        Balance = balance;
    }
}
